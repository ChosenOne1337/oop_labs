package workflow;

public class UnitInfo {
    private String _unitName;
    private String[] _unitArgs;

    public String getUnitName() {
        return _unitName;
    }

    public void setUnitName(String unitName) {
        this._unitName = unitName;
    }

    public String[] getUnitArgs() {
        return _unitArgs;
    }

    public void setUnitArgs(String[] unitArgs) {
        this._unitArgs = unitArgs;
    }
}
