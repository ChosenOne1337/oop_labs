package misc.parser;

public class HandlerTypeParserException extends Exception {
    public HandlerTypeParserException() {
        super();
    }

    public HandlerTypeParserException(String message) {
        super(message);
    }

    public HandlerTypeParserException(String message, Throwable cause) {
        super(message, cause);
    }

    public HandlerTypeParserException(Throwable cause) {
        super(cause);
    }
}
